public class UserAccountData extends BaseData {

    private String FirstName;
    private String LastName;
    private String AccountID;
    private String MobileUserID;
    //private String Email;
    //private String Phone;
    private EmailType Email;
    private PhoneType Phone;
    private String AccountType;
    private String Organization;


    public UserAccountData() {
        super();
    }
    public String getFirstName() {
        return FirstName;
    }

    public void setFirstName(String firstName) {
        FirstName = firstName;
    }

    public String getLastName() {
        return LastName;
    }

    public void setLastName(String lastName) {
        LastName = lastName;
    }

    public String getAccountID() {
        return AccountID;
    }

    public void setAccountID(String accountID) {
        AccountID = accountID;
    }

    public String getMobileUserID() {
        return MobileUserID;
    }

    public void setMobileUserID(String mobileUserID) {
        MobileUserID = mobileUserID;
    }

    public EmailType getEmail() {
        return Email;
    }

    public void setEmail(EmailType email) {
        Email = email;
    }

    public PhoneType getPhone() {
        return Phone;
    }

    public void setPhone(PhoneType phone) {
        Phone = phone;
    }

    public String getAccountType() {
        return AccountType;
    }

    public void setAccountType(String accountType) {
        AccountType = accountType;
    }


    public String getOrganization() {
        return Organization;
    }

    public void setOrganization(String organization) {
        Organization = organization;
    }



    @Override
    public String toString() {
        return UserAccountData.class.getSimpleName()+" email : "+Email;
    }
}
